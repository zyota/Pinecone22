function fullName(firstName, lastName) {
  return firstName + " " + lastName;
}
console.log(fullName("Bayanmunkh", "Enkhbold"));
function sumOfTwo(num1, num2) {
  return num1 + num2;
}
console.log(sumOfTwo(2, 4));
function rectangle(urt, urgun) {
  return urt * urgun;
}
console.log(rectangle(3, 4));
function perimeter(urt, urgun) {
  return (urgun + urt) * 2;
}
console.log(perimeter(2, 3));
